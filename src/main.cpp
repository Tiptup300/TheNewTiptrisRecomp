// ---------------------------------------------------------------------------
// RSP microcode dispatch. Graphics tasks go to the renderer; audio (aspMain)
// would be recompiled here later. Returning nullptr for now (no audio yet).
// ---------------------------------------------------------------------------
// Recompiled TNT audio microcode (rsp/n_aspMain.cpp).
RspExitReason n_aspMain(uint8_t* rdram, uint32_t ucode_addr);

// Safety wrapper: if the microcode hits an audio command whose handler wasn't
// captured by static analysis, report the task as completed rather than letting
// ultramodern treat it as a fatal error and quit the whole game.
static RspExitReason n_aspMain_safe(uint8_t* rdram, uint32_t ucode_addr) {
    RspExitReason reason = n_aspMain(rdram, ucode_addr);
    if (reason != RspExitReason::Broke) {
        return RspExitReason::Broke;
    }
    return reason;
}

static RspUcodeFunc* get_rsp_microcode(const OSTask* task) {
    (void)task; // graphics tasks are routed to the renderer before reaching here;
                // the only other task type this game submits is the audio task.
    return n_aspMain_safe;
}

// ---------------------------------------------------------------------------
// Audio output (SDL). ultramodern hands us interleaved stereo s16 samples that
// the recompiled aspMain microcode produced; we queue them to an SDL device.
// ---------------------------------------------------------------------------
static SDL_AudioDeviceID g_audio_dev = 0;

static void audio_set_frequency(uint32_t freq) {
    if (g_audio_dev != 0) {
        SDL_CloseAudioDevice(g_audio_dev);
        g_audio_dev = 0;
    }
    fprintf(stderr, "[tnt] set_frequency(%u)\n", freq);
    SDL_AudioSpec want{}, have{};
    want.freq = (int)freq;
    want.format = AUDIO_S16SYS;
    want.channels = 2;
    want.samples = 1024;
    want.callback = nullptr; // queue-based
    g_audio_dev = SDL_OpenAudioDevice(nullptr, 0, &want, &have, 0);
    if (g_audio_dev != 0) {
        SDL_PauseAudioDevice(g_audio_dev, 0);
        fprintf(stderr, "[tnt] audio device opened: %d Hz, %d ch\n", have.freq, have.channels);
    } else {
        fprintf(stderr, "[tnt] SDL_OpenAudioDevice failed: %s\n", SDL_GetError());
    }
}

static FILE* g_audio_dump = nullptr;   // raw s16 stereo dump for diagnostics
static uint64_t g_qs_calls = 0, g_qs_samples = 0;

static void audio_queue_samples(int16_t* samples, size_t count) {
    // Guard against a bad/huge buffer from the audio HLE path.
    if (g_audio_dev != 0 && samples != nullptr && count > 0 && count < (1u << 22)) {
        SDL_QueueAudio(g_audio_dev, samples, (Uint32)(count * sizeof(int16_t)));
        if (getenv("TNT_AUDIO_DUMP")) {
            if (!g_audio_dump) g_audio_dump = fopen("/tmp/tnt_audio.raw", "wb");
            if (g_audio_dump) fwrite(samples, sizeof(int16_t), count, g_audio_dump);
        }
        g_qs_calls++; g_qs_samples += count;
        if ((g_qs_calls % 200) == 0) {
            fprintf(stderr, "[tnt] audio: %llu calls, %llu samples, queued=%u bytes\n",
                (unsigned long long)g_qs_calls, (unsigned long long)g_qs_samples,
                (unsigned)SDL_GetQueuedAudioSize(g_audio_dev));
        }
    }
}

#ifdef __linux__
static void tnt_crash_handler(int sig) {
    void* bt[48];
    int n = backtrace(bt, 48);
    fprintf(stderr, "\n[tnt] CRASH: signal %d — backtrace (%d frames):\n", sig, n);
    backtrace_symbols_fd(bt, n, 2);
    signal(sig, SIG_DFL);
    raise(sig);
}
#endif

static size_t audio_get_frames_remaining() {
    if (g_audio_dev == 0) {
        return 0;
    }
    // Two s16 channels per frame.
    return SDL_GetQueuedAudioSize(g_audio_dev) / (2 * sizeof(int16_t));
}

