#define TRACE(msg) do { fprintf(stderr, "[tnt] " msg "\n"); fflush(stderr); } while (0)

int main(int argc, char** argv) {
    (void)argc; (void)argv;

    TRACE("start");
    // Make the app runnable from any directory: assets are resolved relative to
    // the cwd (recompui's get_program_path() returns ""), so chdir to the exe dir.
#ifdef __linux__
    {
        char exe_path[4096];
        ssize_t n = readlink("/proc/self/exe", exe_path, sizeof(exe_path) - 1);
        if (n > 0) {
            exe_path[n] = '\0';
            std::error_code ec;
            std::filesystem::current_path(std::filesystem::path(exe_path).parent_path(), ec);
        }
    }
#endif
    setvbuf(stdout, nullptr, _IONBF, 0); // unbuffered so microcode diagnostics flush
#ifdef __linux__
    signal(SIGSEGV, tnt_crash_handler);
    signal(SIGABRT, tnt_crash_handler);
#endif
    recompui::programconfig::set_program_name("The New Tiptris : Recompiled");
    SDL_SetHint(SDL_HINT_JOYSTICK_THREAD, "1");
    // WSLg / most desktops route audio through PulseAudio; ALSA's "default"
    // device frequently fails there, so prefer PulseAudio/PipeWire before ALSA.
    if (SDL_getenv("SDL_AUDIODRIVER") == nullptr) {
        SDL_setenv("SDL_AUDIODRIVER", "pulseaudio", 1);
    }
    SDL_Init(SDL_INIT_AUDIO);
    NFD_Init();
    fprintf(stderr, "[tnt] audio driver: %s\n", SDL_GetCurrentAudioDriver() ? SDL_GetCurrentAudioDriver() : "(none)");
    TRACE("sdl+nfd init");

    // Config/save location.
    std::filesystem::path config_dir =
        std::filesystem::path(SDL_GetPrefPath("N64Recomp", "TheNewTiptris"));
    recomp::register_config_path(config_dir);
    TRACE("config path registered");

    // Register the recompiled sections and the game.
    tnt::register_overlays();
    TRACE("overlays registered");

    recomp::GameEntry game{};
    game.rom_hash = 0xEDDC4D3AEF798119ULL; // XXH3_64 of the NTSC-U ROM
    game.internal_name = "TETRIS";
    game.display_name = "The New Tiptris";
    game.game_id = u8"tnt.n64.us";
    game.mod_game_id = "tnt";
    game.save_type = recomp::SaveType::AllowAll;
    game.is_enabled = true;
    game.has_compressed_code = false;
    game.entrypoint_address = get_entrypoint_address();
    game.entrypoint = recomp_entrypoint;
    supported_games.push_back(game);
    recomp::register_game(supported_games.back());
    TRACE("game registered");

    recompinput::profiles::initialize_input_bindings();
    // Single-player: assign the keyboard as player 1 so the game sees a controller
    // and receives input (otherwise no device is bound and it reports NO CONTROLLER).
    recompinput::players::set_single_player_mode(true);
    recompinput::playerassignment::add_keyboard_player();
    recompinput::playerassignment::commit_player_assignment();
    TRACE("input bindings init + keyboard assigned as P1");

    // Register the primary UI font (resolved as <program_dir>/assets/<name> by recompui).
    // recompui itself loads assets/recomp.rcss and the fallback fonts during UI init.
    recompui::register_primary_font("fonts/primary.ttf", "primary");
    TRACE("primary font registered");

    // Controls menu: show this game's actions instead of raw N64 button names,
    // and hide the N64 inputs the game doesn't use (bindings still work — this
    // only affects which rows the Controls menu lists).
    {
        using GI = recompinput::GameInput;
        recompinput::set_game_input_name(GI::A,          "Rotate CW");
        recompinput::set_game_input_name(GI::B,          "Rotate CCW");
        recompinput::set_game_input_name(GI::Z,          "Hold");
        recompinput::set_game_input_name(GI::START,      "Start / Pause");
        recompinput::set_game_input_name(GI::DPAD_LEFT,  "Left");
        recompinput::set_game_input_name(GI::DPAD_RIGHT, "Right");
        recompinput::set_game_input_name(GI::DPAD_UP,    "Up");
        recompinput::set_game_input_name(GI::DPAD_DOWN,  "Down");
        recompinput::set_game_input_description(GI::A, "Rotate the piece clockwise (confirm in menus)");
        recompinput::set_game_input_description(GI::B, "Rotate the piece counter-clockwise (back in menus)");
        recompinput::set_game_input_description(GI::Z, "Hold / swap the current piece");
        for (GI gi : { GI::L, GI::R, GI::C_UP, GI::C_DOWN, GI::C_LEFT, GI::C_RIGHT,
                       GI::X_AXIS_NEG, GI::X_AXIS_POS, GI::Y_AXIS_POS, GI::Y_AXIS_NEG }) {
            recompinput::set_game_input_disabled(gi, true);
        }
        recompinput::set_game_input_name(GI::ACCEPT_MENU, "Accept");
        recompinput::set_game_input_name(GI::BACK_MENU,   "Back");
    }

    // Menu theme: a cohesive dark palette (deep charcoal + teal accent) for the
    // launcher and config menus. recompui styles menus inline from these theme
    // colors, so set them before the UI is created.
    {
        using recompui::Color;
        namespace th = recompui::theme;
        th::set_theme_color(th::color::Background1, Color{13, 17, 23, 255});   // deep charcoal
        th::set_theme_color(th::color::Primary,     Color{45, 212, 191, 255}); // teal accent
        th::set_theme_color(th::color::Secondary,   Color{251, 146, 60, 255}); // warm amber
        th::set_theme_color(th::color::Elevated,    Color{45, 212, 191, 38});  // hover/focus fill
        th::set_theme_color(th::color::Text,        Color{230, 237, 243, 255});
        th::set_theme_color(th::color::TextDim,     Color{139, 148, 158, 255});
        th::set_theme_color(th::color::TextActive,  Color{255, 255, 255, 255});
        th::set_border_radius_md(14.0f);
    }

    // Config tabs (general/graphics/sound/controls), then load from disk.
    recompui::config::create_general_tab(recompui::config::GeneralTabOptions{});
    recompui::config::create_graphics_tab();
    recompui::config::create_sound_tab();
    recompui::config::create_controls_tab();
    recompui::config::finalize();
    TRACE("config finalized");

    recomp::rsp::callbacks_t rsp_callbacks{ .get_rsp_microcode = get_rsp_microcode };
    ultramodern::renderer::callbacks_t renderer_callbacks{ .create_render_context = create_render_context };
    ultramodern::audio_callbacks_t audio_callbacks{
        .queue_samples = audio_queue_samples,
        .get_frames_remaining = audio_get_frames_remaining,
        .set_frequency = audio_set_frequency,
    };
    ultramodern::input::callbacks_t input_callbacks{
        .poll_input = recompinput::poll_inputs,
        .get_input = recompinput::profiles::get_n64_input,
        .set_rumble = recompinput::set_rumble,
        .get_connected_device_info = get_connected_device_info,
    };
    ultramodern::gfx_callbacks_t gfx_callbacks{
        .create_gfx = create_gfx,
        .create_window = create_window,
        .update_gfx = update_gfx,
    };
    ultramodern::events::callbacks_t events_callbacks{ .vi_callback = vi_callback };
    ultramodern::error_handling::callbacks_t error_callbacks{ .message_box = recompui::message_box };
    ultramodern::threads::callbacks_t threads_callbacks{ .get_game_thread_name = get_game_thread_name };

    // Test/dev auto-boot: if TNT_ROM points at a ROM, validate+store it, then
    // trigger the game the same way the launcher's "Start Game" button does, but
    // from the launcher update callback so it runs on the UI thread.
    if (const char* rom_env = std::getenv("TNT_ROM")) {
        std::u8string gid = game.game_id;
        recomp::RomValidationError err = recomp::select_rom(std::filesystem::path(rom_env), gid);
        TRACE("auto-boot: select_rom done");
        if (err == recomp::RomValidationError::Good) {
            recompui::register_launcher_update_callback([gid](recompui::LauncherMenu*) {
                static int frame = 0;
                static bool started = false;
                if (!started && ++frame > 120) {
                    started = true;
                    fprintf(stderr, "[tnt] auto-boot: start_game (UI thread)\n"); fflush(stderr);
                    recomp::start_game(gid, {});
                    recompui::hide_all_contexts();
                }
            });
        } else {
            fprintf(stderr, "[tnt] auto-boot: ROM validation failed (%d)\n", (int)err); fflush(stderr);
        }
    }

    TRACE("calling recomp::start");
    recomp::start(
        recomp::Version{1, 0, 0},
        {},
        rsp_callbacks,
        renderer_callbacks,
        audio_callbacks,
        input_callbacks,
        gfx_callbacks,
        events_callbacks,
        error_callbacks,
        threads_callbacks
    );

    return 0;
}
